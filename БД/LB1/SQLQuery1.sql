SELECT        ТОВАРЫ.Наименование, ЗАКАЗЫ.Цена_продажи, ЗАКАЗЫ.Количество, ЗАКАЗЫ.Дата_поставки, ЗАКАЗЫ.Заказчик, ЗАКАЗЫ.Номер_заказа
FROM            ТОВАРЫ INNER JOIN
                         ЗАКАЗЫ ON ТОВАРЫ.Наименование = ЗАКАЗЫ.Наименование_товара
WHERE        (ЗАКАЗЫ.Дата_поставки > CONVERT(DATETIME, '2020-02-10 00:00:00', 102))